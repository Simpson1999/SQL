    SALES			Double(9,2),
CONSTRAINT EmplNum_PK PRIMARY KEY(EMPL_NUM)
);

CREATE TABLE OFFICES  (
    OFFICE   			INTEGER NOT NULL,
    CITY	   		VARCHAR(15) NOT NULL,
    REGION			VARCHAR(10) NOT NULL,
    MGR			   	INTEGER NOT NULL,
    TARGET			DOUBLE(9,0),
    SALES			DOUBLE(9,0),
