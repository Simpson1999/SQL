);
CREATE TABLE ORDERS  (
    ORDER_NUM  			INTEGER NOT NULL,
    ORDER_DAT   		DATE NOT NULL,
    CUST			INTEGER,
    REP			   	INTEGER,
    MFR			CHAR(3) NOT NULL,
    PRODU			CHAR(5) NOT NULL,
    QTY				INTEGER NOT NULL,
    AMOUNT			DOUBLE(9,2),
CONSTRAINT OrderNum_PK PRIMARY KEY(ORDER_NUM)
);
CREATE TABLE PRODUCTS  (
    MFR  			CHAR(3) NOT NULL,
    PRODU   		CHAR(5) NOT NULL,
    DESCRIPTION			VARCHAR(20) NOT NULL,
    PRICE		   	DOUBLE (9,2) NOT NULL,
    QTY_ON_HAND			INTEGER NOT NULL,
CONSTRAINT MfrID_PK PRIMARY KEY(MFR, PRODU)
);

